def send_intelligence_report(company_name: str, recipient_email: str):
    print(f"\n🚀 Generating report for: {company_name}")
    report = run_stratumai(company_name)

    print(f"\n📧 Sending email to: {recipient_email}")
    send_report_email(company_name, report, recipient_email)
    return report


